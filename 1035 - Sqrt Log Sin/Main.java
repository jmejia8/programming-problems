import java.util.Scanner;

class Main {
	public static int termino(int i) {
		// calcula el i-esimo termino de la sucesi√≥n
		if(i==0)
			return 1;
		return (int)Math.sin(1);
	}

	public static void main(String args[]) {
		Scanner entrada = new Scanner(System.in);
		while(true) {
			int numero = Integer.parseInt(entrada.nextLine());



			if(numero < 0)
				break;
		}
	}
}